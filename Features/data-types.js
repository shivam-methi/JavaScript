/* Data Types::

Primitive Types:

1. String: Represents a sequence of characters (text).
*/
let name = "Shivam";


/*
2. Number: Represents both integer and floating-point numbers.
*/
let age = 25;
let price = 99.99;


/*
3. Boolean: Represents logical values true or false.
*/
let isAvailable = true;


/*
4. Undefined: A variable that has been declared but not assigned a value.
*/
let x;
console.log(x); // undefined


/*
5. Null: Represents an intentional absence of any object value.
*/
let person = null;


/*
6. Symbol: Represents a unique identifier, often used for object property keys.
*/
let uniqueID = Symbol('id');


/*
7. BigInt: Used for integers that are too large to be represented by the Number type.
*/
let bigNumber = BigInt(9007199254740991) + BigInt(1);












