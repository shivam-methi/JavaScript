/*
= represents to assignment operator 
assigning values to a variables
*/
console.log(x = 5);



/*
== represents to equality operator 
it compares the value on both sides (left & right) and return true if they are equal
*/
console.log(5 == '5');



/*
=== represents to strict equality operator
it compares the values on both sides (left & right) along with their data types and return true if they are equal
*/
console.log(5 === '5');
